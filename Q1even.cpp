// print all the even number from 1 to 20 using for loop.
#include <iostream>
using namespace std;

int main(){
int number = 20;
cout << "The even numbers from 1 to 20 are: ";

    for (int i = 1; i <= number; i++) {
        if (i % 2 == 0) { 
            cout << i << " "; 
        }
    }
    cout << endl; 
    return 0;

return 0;

}